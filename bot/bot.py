def get_active_users(ctx: ContextTypes.DEFAULT_TYPE) -> dict:
    return ctx.bot_data.setdefault("active_users", {})


async def send_week_lesson(bot, user_id: int, entry: dict):
    """Отправить урок недели конкретному пользователю."""
    level = entry["level"]
    week_idx = entry["week"] - 1
    lessons = PROGRAM.get(level, [])
    if week_idx >= len(lessons):
        return

    lesson = lessons[week_idx]
    name = entry.get("name", "Брат")
    title = lesson["title"]
    text = lesson["text"].replace("{name}", name.split()[0] if name else "Брат")
    total = LEVEL_WEEKS[level]

    # Если tasks — словарь (многоуровневый, как в ВАКТ), показываем все три уровня
    raw_tasks = lesson["tasks"]
    if isinstance(raw_tasks, dict):
        labels = {"А": "🌱 Уровень А", "Б": "📗 Уровень Б", "В": "📘 Уровень В"}
        tasks_parts = []
        for key, label in labels.items():
            lvl_tasks = raw_tasks.get(key, [])
            if lvl_tasks:
                tasks_parts.append(
                    f"*{label}:*\n" + "\n".join(f"  {t}" for t in lvl_tasks)
                )
        tasks = "\n\n".join(tasks_parts)
    else:
        tasks = "\n".join(f"  {t}" for t in raw_tasks)

    msg = (
        f"📅 *{title}*\n"
        f"_{LEVEL_NAMES[level]} · Неделя {entry['week']} из {total}_\n\n"
        f"{text}\n\n"
        f"*Задания на эту неделю:*\n{tasks}"
    )
    try:
        await bot.send_message(
            chat_id=user_id,
            text=msg,
            parse_mode="Markdown",
            reply_markup=InlineKeyboardMarkup([[
                InlineKeyboardButton("📱 Открыть трекер", web_app=WebAppInfo(url=MINIAPP_URL)),
                InlineKeyboardButton("✅ Понял, иду делать", callback_data="week_ack"),
            ]])
        )
    except Exception as e:
        logger.warning(f"Не удалось отправить урок пользователю {user_id}: {e}")


async def job_weekly_lesson(ctx: ContextTypes.DEFAULT_TYPE):
    """Запускается каждый понедельник в 9:00 — рассылает урок всем активным."""
    active = get_active_users(ctx)
    if not active:
        return
    sent = 0
    graduated: list = []
    for uid_str, entry in list(active.items()):
        uid   = int(uid_str)
        level = entry["level"]
        max_weeks = LEVEL_WEEKS.get(level, 8)

        # Участник завершил программу
        if entry["week"] > max_weeks:
            if not entry.get("completed_notified"):
                entry["completed_notified"] = True
                name = entry.get("name", "Участник")
                graduated.append((uid_str, name, level))
                try:
                    await ctx.bot.send_message(
                        chat_id=uid,
                        parse_mode="Markdown",
                        text=(
                            f"🎓 *МашаАллах, {name.split()[0]}!*\n\n"
                            f"Ты завершил *{LEVEL_NAMES[level]}*!\n\n"
                            f"Это большой шаг — ты дошёл до конца. "
                            f"Аллах видит каждое твоё усилие.\n\n"
                            f"🌿 _Твой сертификат готовится — куратор свяжется с тобой._"
                        )
                    )
                except Exception:
                    pass
            continue

        await send_week_lesson(ctx.bot, uid, entry)
        entry["week"] += 1
        sent += 1

    logger.info(f"Еженедельная рассылка: отправлено {sent} уроков, завершили: {len(graduated)}")

    # уведомить куратора
    lines = [f"📬 *Еженедельная рассылка завершена*\n\nОтправлено уроков: *{sent}*"]
    if graduated:
        lines.append("\n🎓 *Завершили программу:*")
        for uid_str, name, level in graduated:
            lines.append(f"  • {name} (`{uid_str}`) — {LEVEL_NAMES[level].split('·')[0].strip()}")
    for cid in CURATOR_IDS:
        try:
            await ctx.bot.send_message(
                chat_id=cid,
                text="\n".join(lines),
                parse_mode="Markdown"
            )
        except Exception:
            pass


async def cmd_activate(update: Update, ctx: ContextTypes.DEFAULT_TYPE):
    """Только для кураторов. /activate <user_id> <уровень> [неделя]"""
    if update.effective_user.id not in CURATOR_IDS:
        await update.message.reply_text("⛔️ Команда только для кураторов.")
        return

    args = ctx.args
    if len(args) < 2:
        await update.message.reply_text(
            "Использование: `/activate <user_id> <А/Б/В> [неделя]`\n\n"
            "Например: `/activate 123456789 Б`\n"
            "Или с недели: `/activate 123456789 В 3`",
            parse_mode="Markdown"
        )
        return

    try:
        target_id = int(args[0])
    except ValueError:
        await update.message.reply_text("❌ Неверный user_id — должно быть число.")
        return

    level = args[1].upper()
    if level not in PROGRAM:
        await update.message.reply_text("❌ Уровень должен быть А, Б, В или Г.")
        return

    week = 1
    if len(args) >= 3:
        try:
            week = max(1, min(int(args[2]), LEVEL_WEEKS[level]))
        except ValueError:
            pass

    active = get_active_users(ctx)
    name = ctx.bot_data.get("user_names", {}).get(str(target_id), "Участник")

    active[str(target_id)] = {
        "level": level,
        "week": week,
        "name": name,
        "activated_at": datetime.now(timezone.utc).isoformat(),
    }

    # Запустить онбординг
    try:
        await run_onboarding(ctx.bot, target_id, name, level, week, ctx)
        participant_notified = "✅ Онбординг запущен"
    except Exception as e:
        participant_notified = f"⚠️ Не удалось запустить онбординг: {e}"

    await update.message.reply_text(
        f"✅ *Активировано*\n\n"
        f"👤 ID: `{target_id}`\n"
        f"📍 Уровень: *{level}* ({LEVEL_NAMES[level]})\n"
        f"📅 Начинает с недели: *{week}*\n"
        f"{participant_notified}",
        parse_mode="Markdown"
    )


