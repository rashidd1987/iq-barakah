    from datetime import date
    today = str(date.today())
    jarwas_usage = ctx.user_data.setdefault("jarwas_usage", {"date": today, "count": 0})
    if jarwas_usage["date"] != today:
        jarwas_usage["date"] = today
        jarwas_usage["count"] = 0
    if jarwas_usage["count"] >= 10:
        await update.message.reply_text(
            "Брат/сестра, на сегодня лимит общения с Джарвасом исчерпан (10 сообщений в день) 🌙\n\n"
            "Возвращайся завтра — Джарвас ждёт тебя! Если срочный вопрос — пиши куратору 🤍",
            reply_markup=InlineKeyboardMarkup([
                [InlineKeyboardButton("💬 Написать куратору", url=SITE)],
            ])
        )
        return
    jarwas_usage["count"] += 1

    # Строим персональный системный промпт
    system = build_jarwas_system(level, week)

    # Берём историю диалога (последние 6 сообщений = 3 обмена)
    history = ctx.user_data.setdefault("jarwas_history", [])
    history.append({"role": "user", "content": user_text})
    if len(history) > 6:
        history = history[-6:]
        ctx.user_data["jarwas_history"] = history

    await ctx.bot.send_chat_action(chat_id=update.effective_chat.id, action="typing")

    # Кнопки по маркерам
    BTN_MAP = {
        "[BTN:diag]":     InlineKeyboardMarkup([[InlineKeyboardButton("🎯 Пройти диагностику", callback_data="start_diag")]]),
        "[BTN:buy_vakt]": InlineKeyboardMarkup([[InlineKeyboardButton("🌱 Начать ВАКТ — 1 500 ₽", callback_data="pay_vakt")]]),
        "[BTN:buy_s1]":   InlineKeyboardMarkup([[InlineKeyboardButton("📗 Начать Сезон 1", callback_data="pay_s1_full")]]),
        "[BTN:curator]":  InlineKeyboardMarkup([[InlineKeyboardButton("💬 Написать куратору", url=SITE)]]),
    }

    try:
        response = _jarwas_client.messages.create(
            model="claude-sonnet-4-6",
            max_tokens=1024,
            system=system,
            messages=history,
        )
        reply = response.content[0].text

        # Ищем маркер кнопки в конце ответа
        markup = None
        for marker, kb in BTN_MAP.items():
            if marker in reply:
                reply = reply.replace(marker, "").strip()
                markup = kb
                break

        history.append({"role": "assistant", "content": reply})
        await update.message.reply_text(reply, parse_mode="Markdown", reply_markup=markup)
    except Exception as e:
        logger.warning(f"Джарвас ошибка: {e}")
        uid_int = update.effective_user.id
        if uid_int in CURATOR_IDS:
            await update.message.reply_text(f"⚠️ [DEBUG куратор] Джарвас ошибка:\n`{e}`", parse_mode="Markdown")
        else:
            await update.message.reply_text(
                "Прости, что-то пошло не так. Попробуй чуть позже 🤍"
            )


